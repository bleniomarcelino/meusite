                                        MOMENTOS MÁGICOS
                                    </div>
                                    <div class="news_resume">
                                      <p><strong>Todo o drama desse incrível esporte com mais visibilidade e demonstrado em<br>
                                       seus diversos estilos de jogo.</strong></p>
                                       
                                      <p>Apresentação de 11 novas habilidades que tornarão os jogadores ainda mais únicos, incluindo giro controlado, passe sem olhar e diferentes tipos de chute. Essas novas habilidades aumentam a quantidade disponível no jogo a 39</p>
                                      
                                      <p>A individualidade do jogador também foi melhorada e suas habilidades e pontos fortes têm mais influência no desempenho e nos movimentos em campo.</p>
                                      
                                      <p>11 novas habilidades<br>
                                            Giro de 180°<br>
                                            Toque duplo<br>
                                            Contr. habil. de domín.<br>
                                            Passe sem olhar<br>
                                            Interceptação<br>
                                            Especialista em pênalti<br>
                                            Pegador de pênaltis<br>
                                            Reposição alta do GOL<br>
                                            Chutes com decolagem<br>
                                            Controle da cavadinha<br>
                                            Chutes com mergulho
                                        </p>
                                        
                                        <p>Agora a animação e a fluidez do controle de bola se baseiam em diversos fatores como posicionamento do jogador e da bola com relação à linha defensiva, fintas precisas sendo feitas para contornar a marcação e consciência da proximidade dos adversários ao virar.</p>
                                        
                                        <p>Novas mecânicas de chute com animações variadas e exclusivas foram adicionadas, incluindo o movimento da bola passando pelas mãos esticadas do goleiro, reações do atacante que perdeu um gol por pouco e reações mais intensas/felizes perto da área.</p>
                                        
                                        <p>Um recurso adicionado importante é a Resistência Visível: um sistema de resistência reprogramado e reequilibrado. Jogadores darão indicativos visuais claros quando estiverem cansados e o nível de resistência influenciará o desempenho. O gerenciamento dos jogadores durante a partida e o momento das substituições pode ser o diferencial entre vencer e perder.</p>
                                        
                                        <p>Substituições rápidas ficarão disponíveis quando a bola sair de jogo. Apenas pressionar um botão vai exibir as alterações recomendadas com base na posição e na resistência. Não precisa mais interromper aquela partida emocionante!.</p>
                                        
                                        <p>Foram feitas melhorias para tornar o movimento da bola mais natural e variado. A trajetória da bola passa a ter influência da postura do jogador e de como foi chutada. Além disso, situações como rebotes do corpo do goleiro ou de defensores ao bloquear terão resultados mais realistas.</p>
                                        
                                        <p>O toque de corpo todo apresentado no ano passado foi melhorado. O domínio da bola passa a depender mais dos arredores, permitindo um domínio mais suave e com mais contexto.</p>
                                        
