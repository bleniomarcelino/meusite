                                       NOVO MYCLUB
                                    </div>
                                    <div class="news_resume">
                                    <strong>As maiores alterações ao myClub em anos. Contrate os grandes craques, jogue cooperativamente ou encare seus rivais na disputa da PES LEAGUE.</strong>
                                      
                                      <p>Além disso, jogadores dos clubes parceiros receberão uma pontuação geral especial com este recurso aprovado pelos próprios clubes.
                                        As imagens desses jogadores serão exibidas no jogo com designs exclusivos de JOGADORES EM DESTAQUE</p>
                                        
                                       <strong>Lendas do myClub</strong> 
                                       
                                      <p>Lendas como Beckham, Maradona, Cruyff, Nedvěd, Gullit, Maldini e Kahn aparecerão no PES 2019. Novas lendas serão adicionadas no futuro.</p>
                                      
                                      <strong>Lista das Lendas do myClub</strong><br>
                                      <img src="../images/legends.png"><br><br>
                                      
                                      
                                      <strong>Melhoria nos valores de compras de jogadores com moedas myClub</strong><br>
                                     
                                      <p>O custo para contratar um jogador passará a ser 100 moedas myClub.
                                        Além disso, haverá empresários que podem contratar 3 jogadores por 250 moedas myClub.
                                        Naturalmente, se você tiver poucas moedas myClub, também teremos muitos empresários que podem ser usados com GP.
                                        Nunca foi tão fácil montar seu time dos sonhos no myClub</p>
                                      
                                      <strong>Apresentação de jogadores repetidos e do sistema de trocas</strong><br>
                                      
                                      <p>Agora você poderá contratar jogadores repetidos. Com isso, se tiver 3 repetidos, você poderá trocá-los por um único jogador da mesma raridade.
                                        Melhore seu time do myClub fazendo trocas mais específicas para contratar os grandes astros ou os jogadores do seu time do coração! Todos os jogadores adquiridos por meio de transações serão Lv. 1.</p>
                                      
                                      <strong>Limite de espaços de jogadores aumentado</strong><br>
                                     
                                      <p>O limite máximo de Agentes e Empresários que você pode contratar agora é 1,000, portanto, não será necessário comprar espaços extras.
                                        Experimente várias combinações de jogadores e estratégias montando diversos times. Treine e teste todos em eventos limitados na página de NOVIDADES.</p>
                                      
                                      <strong>Preparadores de habilidade</strong><br>
                                      
                                      <p>Designe um preparador de habilidade a um jogador para ensinar uma nova habilidade. Aumente o repertório de habilidades do jogador para aumentar sua contribuição ao time.</p>
                                      
                                      <strong>Preparadores de posição</strong><br>
                                      
                                      <p>Designe um preparador de posição a um jogador para aumentar sua familiaridade com uma posição específica.
                                        Quanto mais jogadores do seu time puderem atuar em posições diferentes, mais opções você terá para buscar a vitória!</p>
                                      
                                      <strong>Flexibilidade para obter técnicos</strong><br>
                                      
                                      <p>As listas de técnicos serão atualizadas com maior frequência, o que dará mais chances de obter o técnico com formação e opções de estratégica que você tanto procura.</p>
                                      
                                      <strong>Fique por dentro com as NOVIDADES</strong><br>
                                     
                                      <p>Dispute torneios online realizados por tempo limitado e ganhe bônus especiais</p>
                                      
                                      <strong>Dispute e ganhe Reconhecimento</strong><br>
                                      
                                      <p>Reconhecimento é o novo sistema que representa seu nível de habilidade em partidas 1 x 1 contra outros usuários do myClub. Cada usuário tem um nível de Reconhecimento que pode ser melhorado com mais vitórias, e os vencedores de torneios ainda ganharão títulos de Reconhecimento especiais.</p>
                                      
                                      <strong>Rankings semanais da PES LEAGUE</strong><br>
                                      
                                      <p>Todas as partidas no myClub afetam o ranking, que é usado para determinar sua posição geral de divisão. Além disso, usuários com níveis de habilidade similares disputam uns contra os outros semanalmente no Ranking por grupo, com os melhores de cada grupo recebendo prêmios ao final de cada ciclo semanal.
                                        Confira os torneios a serem realizados diariamente nas NOVIDADES e busque melhores posições e divisões</p>
                                      
                                      <strong>Compare seu time com o dos rivais</strong><br>
                                     
                                      <p>Agora você poderá ver o time de cada usuário na tela de ranking.
                                        Analisar os times dos rivais que o derrotaram ou estão no topo do ranking será uma ferramenta valiosa para montar sua estratégia.</p>
