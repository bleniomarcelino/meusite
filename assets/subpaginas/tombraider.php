                                    <div class="news_title">
                                      Apocalipse Maia e amadurecimento da personagem
                                    </div>
                                    <div class="news_resume">
                                     <p>A história deste novo game mostra, exatamente, como Lara Croft evolui de jovem assustada para uma verdadeira predadora, ou seja, como ela se tornou a Tomb Raider. Nesta conclusão da sua origem, a protagonista continua em sua perseguição implacável à milenar organização secreta militar Trinity, ou Trindade, responsável pela morte de seu pai e que tem a ambição de buscar a imortalidade humana a qualquer preço. Ao mesmo tempo, ela precisa nada menos que impedir um apocalipse Maia. Nem um pouco de pressão, imagina...</p>
                                     
                                     <p>Durante sua busca, a aventureira será confrontada com escolhas que irão defini-la como pessoa e cravar o próprio futuro da humanidade. Diferentemente dos dois primeiros jogos da trilogia, a heroína agora mostra que está mais madura e calculista do que nunca ao encontrar, por exemplo, novas maneiras de sobreviver. Esqueça a jovenzinha perdida e desesperada do primeiro jogo, a nova Lara está brutal, mais racional e com menos paciência para seus inimigos.</p>
                                     
                                     <p>Durante a demonstração que jogamos, o Brasil é mencionado como uma possível rota usada pela Trinity, o que levantou esperanças de que o jogo pudesse dar uma passada por aqui. No entanto, não foi dessa vez. Na verdade, a demo começa com você na ilha de Cozumel, no México, rastreando os passos de um dos chefões da organização secreta, o Dr. Domingues, que busca um artefato que pode criar uma catástrofe mundial em proporção bíblica, ou melhor, Maia. Seu destino final (mencionado no teste) é o Peru, mas até lá muita água vai rolar - literalmente.</p>
                                     
                                     <p>O mais interessante da história de Shadow, até onde jogamos, é a quebra de expectativa em relação ao cataclisma instaurado logo no início da história: é a própria Lara que causa o problema. Não porque ela quis, mas por uma escolha precipitada ao tentar fazer o bem. De forma mais clara, ao explorar uma tumba e conseguir uma adaga Maia, ela “cria” um tsunami na ilha onde está, causando destruição, feridos e mortes. Não ficou claro se o jogador poderá controlar e tomar as decisões pela protagonista, mas certamente o que a própria Lara decidir fazer, de agora em diante, terá uma consequência muito mais profunda na história.</p>
                                     
                                     <p>O retorno da sua parceria com o amigo de longa data, Jonas, é onde fica evidente para o jogador que nem tudo o que ela faz, que nem todas as suas decisões, por melhores que sejam suas intenções, são as mais adequadas no momento. Pela primeira vez, alguém próximo a Lara confronta suas ideias e mostra que nem tudo no mundo gira em torno de Lara Croft, e que podem existir outras prioridades.</p>
