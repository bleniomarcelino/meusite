                                        Tomb Raider - shadow of the
                                    </div>
                                    <div class="news_resume">
                                     <p>A Eidos Montreal e a Square Enix partilharam um novo teaser de Shadow of the Tomb Raider, repleto de obstáculos e perigos.</p>
                                     <p>Neste pequeno teaser, com cerca de 30 segundos, podes ver Lara Croft em momentos de incrível perigo, perante obstáculos ameaçadores e em momentos acrobáticos que exigem imenso das suas capacidades.</p>
                                     <p>É uma pequena amostra do que encontrará em Shadow of the Tomb Raider e revela bem o espírito que a equipa pretende para o seu novo jogo.</p>
                                     <p>Shadow of the Tomb Raider chegará em 14 de Setembro.</p>
