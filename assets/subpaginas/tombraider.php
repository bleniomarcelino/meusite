                                       Tumbas claustrofóbicas e controles já conhecidos
                                    </div>
                                    <div class="news_resume">
                                     <p>Ainda sobre as cavernas, ou tumbas, os produtores prometem que neste game elas estarão aterrorizantes, esta foi exatamente a palavra que usaram. No modo singleplayer, a promessa é entregar uma experiência claustrofóbica, angustiante e mortal. Errar algum puzzle, por exemplo, poderá representar o seu fim. Aparentemente, tudo neste jogo quer te matar, inclusive o cenário. Já no modo multiplayer, os produtores comentaram que um DLC futuro trará um opção de exploração de algumas dessas tumbas em cooperação com um amigo. No entanto, nenhum outro detalhe a respeito foi revelado.</p>
                                     
                                     <p>A respeito dos controles, sem muita novidade por aqui, especialmente se você for um veterano da trilogia. Vale destacar, entretanto, que o sistema de stealth, ou furtividade, está melhor. Além de poder usar arbustos como esconderijo, Lara também pode usar a própria vegetação pelas paredes para se esconder ou encurralar algum inimigo. Ataques furtivos geram XP (pontos de experiência) extra e, assim como em outros jogos da série, é possível ativar uma breve animação ao finalizar o adversário com o gancho de escalada, por exemplo, em modo furtivo.</p>
                                    
