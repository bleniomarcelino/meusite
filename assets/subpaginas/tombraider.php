                                      <p>Se Shadow of the Tomb Raider já impressiona pelo visual nos consoles, nos PCs eles serão ainda melhores. Isso graças a uma parceria da Square Enix com a NVidia, que permitirá aproveitar ao máximo os benefícios em performance das GPUs GeForce GTX série 10.</p>
                                      
                                      <p>A parceria é antiga, desde o game anterior que já trazia gráficos superiores nas versões para PC, possibilitando que milhares de jogadores pudessem aproveitar os recursos e performance superior da plataforma GeForce GTX.</p>
