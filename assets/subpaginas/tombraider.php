                                      Lara mais brutal e que agora sabe nadar
                                    </div>
                                    <div class="news_resume">
                                       <p>Em Shadow of the Tomb Raider, Lara está mais brutal e focada em seus objetivos do que nunca. Mais madura, ela aprendeu novos modos para sobreviver na selva. É possível agora, por exemplo, usar lama como camuflagem e explorar passagens embaixo d'água. Inclusive, esta opção submersa é uma das novidades do gameplay. Durante os testes, pudemos mergulhar para encontrar rotas alternativas em uma caverna, além de tentarmos sobreviver a um tsunami.</p>
                                       
                                       <p>A mecânica para nadar segue um padrão parecido de diversos outros jogos de ação e aventura: botão para mergulhar e emergir; botão para nadar mais rápido e até mesmo um “botão de pânico”, caso você esteja quase sem ar e consiga encontrar um cantinho de emergência para tomar um fôlego. O tsunami, causado pela Lara diga-se de passagem, lembra muito a desesperadora parte das corredeiras de Tomb Raider, primeiro jogo da trilogia. Além de desviar de obstáculos que estão boiando, enquanto é varrido pela força das águas, agora é preciso também tentar não morrer ao mergulhar enquanto encontra algum lugar mais seguro em meio ao caos.</p>
                                       
                                       <p>Um pulo, ou mesmo um caminho mal escolhido, e no tempo errado, representa morte certa. Experimentamos, por exemplo, uma morte nada agradável com uma estaca enorme atravessada pela barriga da personagem. Sim, as mortes brutais do jogo estão de volta. Por isso, a dica é: seja ágil, especialmente quando tudo começar a desmoronar em sua volta. Pular, correr, ou mesmo andar no tempo errado é game over. Mas cuidado: ser ágil não é só sair correndo pelo cenário. Há armadilhas escondidas pelo chão nas cavernas. É possível desativá-las, se você as vir em tempo, caso contrário seu destino pode ser virar espetinho em meio a lanças que saem das paredes.</p>
                                     </div> 
                                     
                                     
