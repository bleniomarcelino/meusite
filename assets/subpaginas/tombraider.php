                                       Cenários bem detalhados e Lara mais sarada 
                                    </div>
                                    <div class="news_resume">
                                        <p>Visualmente falando, umas das primeiras coisas que chama a atenção é o físico mais definido de Croft. Apesar de ainda usar o mesmo penteado, com cabelo à prova de apocalipse - já que está sempre razoavelmente arrumado -, Lara está mais forte fisicamente, com braços e pernas mais definidos.</p>
                                        
                                         <p>Por falar em cenários, Shadow of the Tomb Raider traz uma ambientação três vezes maior que o jogo antecessor, segundo os produtores. O jogo não é do tipo mundo aberto, mas oferece detalhes ao redor que encantam, como as ondas ferozes do tsunami e do interior sombrio da caverna explorada na demo.</p>
                                         
                                         <p>A física do ambiente, ou seja, a quantidade objetos do cenário que é possível mover ou destruir, não traz muito de extraordinário, ao menos nos testes. Mesmo assim foi possível perceber a atenção especial dada às cenas de explosões e prédios desmoronando, por exemplo. Durante a demo, no entanto, não foi possível verificar se era possível escolher partes do cenário para destruir; apenas locais pré-determinados pelo game era quebráveis.</p>
                                         
                                         <p>A versão demonstração também não mostrou como ficará o mapa do jogo, nem mesmo o sistema de caça ou de confecção de armas e trajes. Era apenas possível coletar as matérias-prima e munições.</p>
